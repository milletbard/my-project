<template>
  <div class="row">
    <div class="col-sm-12">
      <h3>Score: {{ grade }}</h3>
      <h3>Total: {{ Total }}</h3>
      <h2 class="animated fadeIn" v-if="question" :key="question.english">
        Q : {{ question.trans }}
        <div class="status">{{ status }}</div>
      </h2>
      <ul class="animated fadeIn">
        <li
          v-bind:key="option.english"
          v-bind:class="{correct: correct ==  'correct' && option.correct ,
          wrong:wrong == 'wrong' && option.english ==wrongOption.english
          }"
          v-for="option in options"
          v-on:click="check(option)"
        >{{option.english}}</li>
      </ul>
    </div>
  </div>
</template>

<script>
export default {
  name: "english",
  data() {
    return {
      words: rows4,
      question: null,
      options: [],
      grade: 0,
      Total: 0,
      wrongOption: {},
      correct: "",
      wrong: ""
    };
  },
  mounted() {
    this.pick();
  },
  methods: {
    check(option) {
      this.Total++;
      this.wrongOption = option;
      if (option.correct) {
        this.status = "correct";
        this.grade++;
      } else {
        this.correct = "correct";
        this.wrong = "wrong";
      }
      setTimeout(() => {
        this.correct = "";
        this.wrong = "";
        this.pick();
      }, 1500);
    },
    pick() {
      //隨機單字
      this.question = this.words[parseInt(Math.random() * this.words.length)];
      this.options = this.getOptions(this.question);
    },
    getOptions(question) {
      let result = this.words
        .filter(word => word.cata == question.cata)
        .filter(word => word.english[0] == question.english[0])
        .filter(word => word.english != question.english)
        .slice()
        .sort((a, b) => Math.random() - 0.5)
        .slice(0, 4);
      let questionClone = JSON.parse(JSON.stringify(question));
      questionClone.correct = true;
      let result2 = result
        .concat([questionClone])
        .sort((a, b) => Math.random() - 0.5);
      return result2;
    }
  }
};

